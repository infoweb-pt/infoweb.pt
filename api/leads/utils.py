def is_disposable_email(email: str) -> bool:
    """Return True if the email's domain is on the disposable-mail blocklist."""
    try:
        domain = email.strip().lower().split("@", 1)[1]
    except IndexError:
        return False
    return domain in DISPOSABLE_EMAIL_DOMAINS
